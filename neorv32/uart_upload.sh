#!/usr/bin/env bash
# =============================================================================
# uart_send.sh
# Interactive NEORV32 UART uploader
# =============================================================================

set -e

# -----------------------------------------------------------------------------
# Colors
# -----------------------------------------------------------------------------

RED='\033[0;31m'
YELLOW='\033[1;33m'
BLUE1='\033[1;34m'
RESET='\033[0m'

# -----------------------------------------------------------------------------
# Check arguments
# -----------------------------------------------------------------------------

if [ $# -ne 1 ]; then
    echo
    echo "Usage:"
    echo "  ./uart_send.sh <file.bin> "
    echo
    exit 1
fi

BINFILE="$1"

# -----------------------------------------------------------------------------
# Detect FTDI serial port
# -----------------------------------------------------------------------------

PORT=$(find /dev/serial/by-id -name '*FTDI*' | head -n 1)

if [ -z "$PORT" ]; then
    echo -e "${RED}ERROR: No FTDI serial port found.${RESET}"
    exit 1
fi

echo -e "${YELLOW}Using UART port: $PORT${RESET}"


BAUD=19200

# -----------------------------------------------------------------------------
# Check binary
# -----------------------------------------------------------------------------

if [ ! -f "$BINFILE" ]; then
    echo
    echo "ERROR: Binary file not found:"
    echo "  $BINFILE"
    echo
    exit 1
fi


# -----------------------------------------------------------------------------
# Banner
# -----------------------------------------------------------------------------

echo
echo "===================================================="
echo "            NEORV32 UART UPLOADER"
echo "===================================================="
echo
echo "Binary : $BINFILE"
echo "UART   : $PORT"
echo "Baud   : $BAUD"
echo

# -----------------------------------------------------------------------------
# Step 1
# -----------------------------------------------------------------------------

echo "----------------------------------------------------"
echo "Program the FPGA bitstream."
echo "----------------------------------------------------"
echo

read -p "Press ENTER when the FPGA is programmed..."

# -----------------------------------------------------------------------------
# Step 2 - Open UART terminal
# -----------------------------------------------------------------------------

echo -e "${BLUE1}>>> Opening serial monitor in a new terminal...${RESET}"

gnome-terminal -- bash -c "
picocom -b $BAUD \"$PORT\"
exec bash
"
# -----------------------------------------------------------------------------
# Step 2 - Reset CPU / Abort autoboot
# -----------------------------------------------------------------------------

echo
echo "----------------------------------------------------"
echo "Reset the CPU now."
echo
echo "When you see:"
echo
echo "  Auto-boot in 8s. Press any key to abort."
echo
echo "Press SPACE in the UART terminal to stop autoboot."
echo "----------------------------------------------------"
echo

read -p "Press ENTER once autoboot is aborted..."
# -----------------------------------------------------------------------------
# Step 3
# -----------------------------------------------------------------------------

echo
echo "----------------------------------------------------"
echo "In the UART terminal:"
echo
echo "  Type: u"
echo
echo "You should see:"
echo
echo "  Awaiting neorv32_exe.bin..."
echo "----------------------------------------------------"
echo

read -p "Press ENTER once the bootloader is waiting for upload..."

# -----------------------------------------------------------------------------
# Configure UART
# -----------------------------------------------------------------------------

echo
echo "----------------------------------------------------"
echo "Configuring UART..."
echo "----------------------------------------------------"

stty -F "$PORT" \
    "$BAUD" \
    raw \
    -echo \
    -icanon \
    min 1 \
    time 1 \
    cs8 \
    -cstopb \
    -ixon \
    clocal \
    cread

# -----------------------------------------------------------------------------
# Open UART
# -----------------------------------------------------------------------------

exec 3>"$PORT"

FILESIZE=$(stat -c%s "$BINFILE")

sleep 2

echo
echo "Sending binary..."
echo
echo "File size: $FILESIZE bytes"
echo

# -----------------------------------------------------------------------------
# Upload
# -----------------------------------------------------------------------------

cat "$BINFILE" >&3

# -----------------------------------------------------------------------------
# Done
# -----------------------------------------------------------------------------

echo
echo "===================================================="
echo " Upload finished"
echo "===================================================="
echo
echo "Check the UART terminal."
echo
echo "If you received:"
echo
echo "  OK"
echo
echo "then type:"
echo
echo "  e"
echo
echo "to execute the program."
echo
echo "===================================================="

exec 3>&-
